            CONTROLLI

MOVIMENTO:

Il gioco è controllato con il mouse. Click sinistro sulle posizioni che si desidera spostare. Tenere premuto il pulsante per continuare a muoversi in quella direzione. Con CTRL premuto puoi correre.

ATTACCO:

Tasto sinistro del mouse su un nemico causerà Tux venire vicino il robot e iniziano ad attaccare con l'arma. Se l'attuale arma è una pistola Tux stare fermo e sparera il robot.

FUNZIONI SPECIALI:

Usa il tasto destro per attivare l' abilità/programma attualmente selezionato.

Tasti Normali (la maggior parte dei tasti possono essere riassegnata nel game con il tasto - F1)

  Uscita ... Mostra il menu principale
  F1 ... Mostra la keychart
  F2 ... Attiva schermo pieno (solo Linux)
  F3 ... Quicksave
  F4 ... Caricamento rapido
  F5-F12 ... Rapida selezione programmi da 1 a 8

  I ... Pannello inventario
  C ... Pannello personaggi
  S ... Pannello abilità/programma
  Spazio ... Chiudi tutti i pannelli aperti
  Q ... Registro delle ricerche
  0-9 ... Rapido inventario degli articoli 0-9

  P ... Pausa
  Premi A ... Attacca mentre rimani nella stessa posizione
  Premi Shift ... Mantieni l'obiettivo corrente selezionato quando ti muovi
  Tab        ... Toggle automap

  R ... Ricarica l'arma
  Premi Ctrl ... Corri
  U ... Attiva/disattiva sempre la corsa

  Premi X ... Mostra le etichette degli oggetti sul terreno
  Z ... Attiva/disattiva il mostra etichette degli oggetti sul terreno
  T          ... Toggle walls becoming transparent when Tux approaches (OpenGL only)

TAKEOVER/CHAT

Per chattare con un droide amichevole, è sufficiente cliccare su di lui. To take over a hostile bot, select the takeover/chat skill from the skills screen, then click the right mouse button to initiate takeover.

Per la takeover partita, è possibile utilizzare la rotellina del mouse o i tasti cursore.

Se sei interessato a giocare il Tutorial, è possibile accederelo dal menu principale.

Se si sono bloccati e hai bisogno di aiuto, vieni discutere a #freedroid su IRC.freenode.net e chiedere aiuto.

Questo è tutto, buono gioco.



Premere il tasto sinistro del mouse per iniziare a giocare.
]]
